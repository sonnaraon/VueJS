<template>
    <div>
        <div v-for="ask in asks" v-bind:key="ask in asks">{{ ask.title }}</div>
    </div>
</template>

<script>
import { fetchAskList } from '../api/index.js'

export default {
    data(){
        return {
            asks: []
        }
    },
    created(){
        var vm = this;
        fetchAskList()
            .then(function(response){
                console.log(response);
                vm.asks = response.data;
            })
            .catch(function(error){
                console.log(error);
            });
    }
}
</script>

<style scoped>
    .news {
        display: flex;
        padding: 8px;
    }
</style>